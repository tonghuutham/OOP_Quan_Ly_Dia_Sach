    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here: ADD
        ProductDAO pdDAO = new ProductDAOImpl();
        String Id = jTextField3.getText();
        if ("".equals(Id)){
            jLabel19.setVisible(true);
            jLabel21.setText("Không được bỏ trống ID");
            jLabel21.setVisible(true);
            return;
        }
        if ("".equals(jTextField6.getText())){
            jLabel19.setVisible(true);
            jLabel21.setText("Nhập số lượng!");
            jLabel21.setVisible(true);
            return;
        }
        int so_luong = Integer.valueOf(jTextField6.getText());
        if ("".equals(jTextField7.getText())){
            jLabel19.setVisible(true);
            jLabel21.setText("Nhập đơn giá!");
            jLabel21.setVisible(true);
            return;
        }
        int don_gia = Integer.valueOf(jTextField7.getText());
       
        if (product == null){
            if (pdDAO.getByID(Integer.valueOf(Id)) != null){
                jLabel19.setVisible(true);
                jLabel21.setText("Đã có sản phẩm!");
                jLabel21.setVisible(true);
                return;
            }
            // chưa có sản phẩm
            String tenSP = jTextField4.getText();
            String loaiSP = jTextField5.getText();
            Product pd = new Product(tenSP, so_luong, don_gia, 0, loaiSP, null);
            pd.setID(Integer.valueOf(Id));
            product = pd;
            //String name, int amount,int inPrice, int outPrice,  String type, String category
        }
        
        NhapKho nhapKho = new NhapKho(nhanVien, product, ncc, so_luong, don_gia, date);
        listNhapKho.add(nhapKho);
        
        String data[][] = new String[1][5];
        data[0][0] = String.valueOf(nhapKho.getSanPham().getID());
        data[0][1] = nhapKho.getSanPham().getName();
        data[0][2] = String.valueOf(nhapKho.getSoLuong());
        data[0][3] = String.valueOf(nhapKho.getGiaNhap());
        data[0][4] = nhapKho.getSanPham().getType();
        sum += nhapKho.getSoLuong()*nhapKho.getGiaNhap();
        dtm.addRow(data[0]);
        
        jTable1.setModel(dtm);
        jLabel1.setText(String.valueOf(formatDecimal(sum)));
        jLabel1.setVisible(true);
        jLabel17.setVisible(true);
        jLabel22.setVisible(true);
        product = null;
        
        jLabel19.setVisible(false);
        jLabel21.setVisible(false);
        
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        //NhanVien nhanVien, Product sanPham, NCC ncc, int soLuong, int giaNhap, LocalDate ngayNhap
    }//GEN-LAST:event_jButton5ActionPerformed
