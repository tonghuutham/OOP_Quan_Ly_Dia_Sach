    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        NhapKhoDAO nkDAO = new NhapKhoDAOImpl();
        ProductDAO pdDAO = new ProductDAOImpl();
        Product pd;
        for (NhapKho nk: listNhapKho){
            nkDAO.add(nk);
            pd = pdDAO.getByID(nk.getSanPham().getID());
            if (pd == null){
                pdDAO.add(nk.getSanPham());
            }else{
                pd.setAmount(pd.getAmount()+nk.getSoLuong());
                pdDAO.update(pd);
            }
        }
        setVisible(false);
        new NhapKhoUI().setVisible(true);
    }//GEN-LAST:event_jButton7ActionPerformed

