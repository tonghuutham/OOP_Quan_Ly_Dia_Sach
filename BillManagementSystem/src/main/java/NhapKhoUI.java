    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        String sdt = jTextField2.getText();
        NCCDAO nccDAO = new NCCDAOImpl();
        String sql = "select * from NCC where sdt LIKE '%"+sdt+"'";
        System.out.println(sql);
        ConnectionDB connector = new ConnectionDB();
        Connection con = connector.getConnection();
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            rs.next();
            jTextField1.setText(rs.getString("ten"));
            jTextField2.setText(rs.getString("sdt"));
            ncc = nccDAO.getByID(rs.getInt("ID"));
            jLabel8.setVisible(false);
            jLabel18.setVisible(false);
            
        } catch (SQLException ex) {
            jLabel18.setText("Không tìm thấy công ty!");
            jLabel18.setVisible(true);
            jLabel8.setVisible(true);
            jTextField1.setText("");
        }
        try {
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(NhapKhoUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextField2ActionPerformed

